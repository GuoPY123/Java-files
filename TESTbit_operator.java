public class TESTbit_operator {
    public static void main(String[] args) {
        System.out.println(5 & 9);
        System.out.println(5 | 9);
        System.out.println(~-5);
        System.out.println(5 ^ 9);
        System.out.println(5 << 2);
        System.out.println(-5 << 2);
        System.out.println(-5 >> 2);
        System.out.println(-5 >>> 2);
    }
}
